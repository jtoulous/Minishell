#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	z;

	if (!s || !f)
		return ;
	z = 0;
	while (s[z])
	{
		f(z, &s[z]);
		z++;
	}
}
