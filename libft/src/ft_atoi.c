int	ft_atoi(const char *nptr)
{
	int			i;
	long int	n;
	int			s;

	s = 1;
	i = 0;
	n = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			s = -1;
		i++;
	}
	while (nptr[i] <= '9' && nptr[i] >= '0')
	{
		n = (n * 10) + (nptr[i] - '0');
		i++;
	}
	return (n * s);
}
