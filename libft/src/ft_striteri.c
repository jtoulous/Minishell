void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	x;

	if (!s || !f)
		return ;
	x = 0;
	if (s)
	{
		while (s[x] != '\0')
		{
			(*f)(x, &s[x]);
			x++;
		}
	}
}
