#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tmp;
	size_t			z;

	z = 0;
	tmp = (unsigned char *)s;
	while (z < n)
	{	
		tmp[z] = '\0';
		z++;
	}
}
