#include "libft.h"

void	mega_close(int fd1, int fd2, int fd3, int fd4)
{
	if (fd1 != -1)
		close (fd1);
	if (fd2 != -1)
		close (fd2);
	if (fd3 != -1)
		close (fd3);
	if (fd4 != -1)
		close (fd4);
}
